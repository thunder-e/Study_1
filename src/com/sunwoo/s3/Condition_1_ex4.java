package com.sunwoo.s3;

public class Condition_1_ex4 {
	
	public static void main(String [] args) {
		
		//if(조건식) { }
		// 조건식 - 어떤 연산을 하든 결과물이 boolean값 한개를 도출
		// 결과값이 boolean인 연산자 : 논리, 관계(and,or), not
		
		boolean check = true;

		//어떤 연산에 의해서 check값 변동
		
		if(check) { //check==true
		
		}
		
		if(!check) { //check==false
			//check가 false면 실행
		}
		
		
	}

}
